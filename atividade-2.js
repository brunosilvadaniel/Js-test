/* '==' é usada para comparar apenas o valor por exemplo se comparar as duas variaveis abaixo vai retornar true*/
let a = 1;
let b = '1';

a == b ? console.log('Os valores conhecidem') : console.log('Os valores não conhecidem')

/* '===' é usada para comparar o valor e tipo, por exemplo se comparar as duas variaveis abaixo vai retornar false*/

a === b ? console.log('Os valores conhecidem') : console.log('Os valores não conhecidem')
